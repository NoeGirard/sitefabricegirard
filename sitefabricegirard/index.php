                <br><br>
                La consultation d’un pédicure-podologue
                La pédicurie traite toutes les douleurs et gênes du pied. Cela va de l’ongle incarné aux appuis douloureux qui entraînent la formation de lésions comme des callosités, des cors ou des durillons. Les soins de la pédicurie sont curatifs et soulagent très rapidement. Ce sont des actes de petite chirurgie.
                <br><br>
                La podologie traite des problèmes de mauvaise répartition du poids du corps au niveau de la surface plantaire et des conséquences qui en découlent sur le pied (déformation, hyper appuis), et sur le reste du corps (douleurs articulaires, déformations, problème de colonne vertébrale). Les solutions proposées par la podologie vont du conseil de chaussage à la conception d’appareillage (semelles orthopédiques par exemple).
                <br><br>
